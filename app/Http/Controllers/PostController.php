    public function comment($id, Request $request)
    {
        $post = Post::findOrFail($id);

        $comment = $post->comment()->create([
            'body' => $request->body
        ]);

        // 触发事件
        event(new PostCommented($comment));

        return redirect()->route('post.show', $id);
    }
